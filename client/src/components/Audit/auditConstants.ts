export function getActionColor(action: AuditAction): 'default' | 'primary' | 'secondary' | 'error' | 'warning' | 'success' | 'info' {
  if (['LOGIN', 'LOGIN_OAUTH', 'LOGIN_TOTP', 'LOGIN_SMS', 'REGISTER', 'SESSION_START'].includes(action)) return 'success';
  if (['LOGOUT', 'VAULT_LOCK', 'SECRET_READ', 'SECRET_EXTERNAL_ACCESS', 'SESSION_END', 'SESSION_TIMEOUT', 'RECORDING_VIEW'].includes(action)) return 'default';
  if (['LOGIN_FAILURE', 'DELETE_CONNECTION', 'DELETE_FOLDER', 'UNSHARE_CONNECTION', 'SECRET_DELETE', 'SECRET_UNSHARE', 'TENANT_DELETE', 'TEAM_DELETE', 'GATEWAY_DELETE', 'TENANT_REMOVE_USER', 'TEAM_REMOVE_MEMBER', 'GATEWAY_UNDEPLOY', 'GATEWAY_TEMPLATE_DELETE', 'SESSION_ERROR', 'SFTP_DELETE', 'SECRET_EXTERNAL_REVOKE', 'SESSION_TERMINATE', 'REFRESH_TOKEN_REUSE', 'IMPOSSIBLE_TRAVEL_DETECTED', 'ANOMALOUS_LATERAL_MOVEMENT', 'DB_QUERY_BLOCKED', 'DB_FIREWALL_RULE_DELETE', 'DB_MASKING_POLICY_DELETE', 'RECORDING_DELETE', 'SESSION_TERMINATED_POLICY_VIOLATION', 'TOKEN_HIJACK_ATTEMPT'].includes(action)) return 'error';
  if (['PASSWORD_CHANGE', 'PASSWORD_REVEAL', 'TOTP_ENABLE', 'TOTP_DISABLE', 'SMS_MFA_ENABLE', 'SMS_MFA_DISABLE', 'SECRET_EXTERNAL_SHARE', 'SSH_KEY_ROTATE', 'SSH_KEY_AUTO_ROTATE', 'TENANT_MFA_POLICY_UPDATE', 'VAULT_AUTO_LOCK', 'GATEWAY_RECONCILE'].includes(action)) return 'warning';
  if (['CREATE_CONNECTION', 'CREATE_FOLDER', 'VAULT_SETUP', 'SECRET_CREATE', 'TENANT_VAULT_INIT', 'TENANT_CREATE', 'TEAM_CREATE', 'GATEWAY_CREATE', 'SSH_KEY_GENERATE', 'GATEWAY_TEMPLATE_CREATE', 'SFTP_MKDIR', 'CONNECTION_FAVORITE', 'DB_FIREWALL_RULE_CREATE', 'DB_MASKING_POLICY_CREATE', 'RECORDING_START'].includes(action)) return 'info';
  return 'primary';
}

export function formatDetails(details: Record<string, unknown> | null): string {
  if (!details) return '';
  return Object.entries(details)
    .map(([key, value]) => {
      if (Array.isArray(value)) return `${key}: ${value.join(', ')}`;
      return `${key}: ${value}`;
    })
    .join(' | ');
}

